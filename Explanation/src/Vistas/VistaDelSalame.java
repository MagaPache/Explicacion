    private void cargarTabla() {
        GestorProducto gp = new GestorProducto();
        DefaultTableModel model = new DefaultTableModel();
        Object[] columnas = {"Codigo", "Nombre", "Precio", "Tipo", "Acepta Devoluci√≥n"};
        model.setColumnIdentifiers(columnas);
        for (vmProducto producto : gp.obtenerProductos()) {
            Object[] fila = new Object[5];
            fila[0] = producto.getCodigoProducto();
            fila[1] = producto.getNombre();
            fila[2] = producto.getPrecio();
            fila[3] = producto.getTipoProducto();
            if(producto.isDevolucion()){
                fila[4] = "SI";
            }else{
                fila[4] = "NO";
            }            
            model.addRow(fila);
        }
        tblResultado.setModel(model);
    }

